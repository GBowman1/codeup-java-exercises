public class StringExercise {
    public static void main(String[] args) {
        String one = "We don't need no education\nWe don't need no thought control";
        System.out.println(one);
        String two = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(two);
        String three = "In windows, the main drive is usually C:\\ ";
        System.out.println(three);
        String backslash = "\\";
        System.out.println("I can do backslashes " + backslash + ", double backslashes " + backslash + backslash + ",\n" +
                "and the amazing triple backslash " + backslash + backslash + backslash + "!");

    }

}
